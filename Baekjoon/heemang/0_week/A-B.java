import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // num1과 num2를 입력 받는다.
        int num1 = sc.nextInt();
        int num2 = sc.nextInt();

        // num1 - num2
        System.out.println(num1 - num2);
    }
}
